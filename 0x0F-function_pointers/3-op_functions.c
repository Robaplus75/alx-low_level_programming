#include "3-calc.h"

/**
 * op_add - sum of two nums
 * @num1: num1
 * @num2: num2
 *
 * Return: sum of num1 and num2
 */
int op_add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * op_sub - difference of two nums
 * @num1: num1
 * @num2: num2
 *
 * Return: difference of num1 and num2
 */
int op_sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * op_mul - product of two nums
 * @num1: num1
 * @num2: num2
 *
 * Return: product of num1 and num2
 */
int op_mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * op_div - division of two nums
 * @num1: num1
 * @num2: num2
 *
 * Return: result of the division of num1 and num2
 */
int op_div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * op_mod - calculates the remainder of the division of two integers
 * @num1: nuum1
 * @num2: num2
 *
 * Return: remainder of num1 divided num2
 */
int op_mod(int num1, int num2)
{
	return (num1 % num2);
}
